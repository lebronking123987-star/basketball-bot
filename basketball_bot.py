def get_week_number():
    """Номер недели от старта (26 февраля 2026)"""
    start = datetime(2026, 2, 26)
    now = datetime.now()
    delta = (now - start).days
    return max(0, delta // 7) % 13  # цикл из 13 недель

async def send_poll(chat_id: int, question: str):
    await bot.send_poll(
        chat_id=chat_id,
        question=question,
        options=["🔥 Иду", "❌ Не иду", "💭 Подумаю"],
        is_anonymous=False,
    )

async def scheduler():
    while True:
        now = datetime.now()
        weekday = now.weekday()  # 0=пн, 1=вт, 2=ср, 3=чт
